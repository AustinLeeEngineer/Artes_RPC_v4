// This function is automatically called by the HAL if the UART hardware crashes
void HAL_UART_ErrorCallback(UART_HandleTypeDef *huart)
{
    if (huart->Instance == USART2)
    {
        // 1. Force the HAL state machine to reset from BUSY to READY
        // This clears all error flags and unlocks the UART handle.
        HAL_UART_AbortReceive(huart);

        // 2. Now that the library is reset, re-arm the interrupt!
        HAL_UART_Receive_IT(&huart2, &rx_byte, 1);
    }
}

